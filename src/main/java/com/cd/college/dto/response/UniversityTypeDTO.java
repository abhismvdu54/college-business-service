package com.cd.college.dto.response;

public class UniversityTypeDTO {
	private int universityTypeId;
	private String universityTypeCode;
	private String universityTypeDesc;
	
	public int getUniversityTypeId() {
		return universityTypeId;
	}
	public void setUniversityTypeId(int universityTypeId) {
		this.universityTypeId = universityTypeId;
	}
	public String getUniversityTypeCode() {
		return universityTypeCode;
	}
	public void setUniversityTypeCode(String universityTypeCode) {
		this.universityTypeCode = universityTypeCode;
	}
	public String getUniversityTypeDesc() {
		return universityTypeDesc;
	}
	public void setUniversityTypeDesc(String universityTypeDesc) {
		this.universityTypeDesc = universityTypeDesc;
	}
}
